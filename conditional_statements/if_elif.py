windows =input("Enter Windows version (7/8/10/11): ").lower()

if windows == "7":
    print("Windows 7 was released in 2009")
elif windows == "8":
    print("Windows 8 was released in 2012")
elif windows == "10":
    print("Windows 10 was released in 2015")
elif windows == "11":
    print("Windows 11 was released in 2021")
else:
    print("Unknown Windows version")
